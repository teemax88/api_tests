# Квантификатор { }
# позволяет указать точное число или диапазон раз, когда предыдущий символ или группа должны появиться в шаблоне.
# Синтаксис этого квантификатора:
# {m}
# {m,n}
# {m,}
# где m — минимальное количество вхождений, n — максимальное количество вхождений, а вторая и третья формы необязательны

# Вот несколько примеров
# {3} соответствует ровно трем вхождениям предыдущего символа или группы;
# {1,3} соответствует 1–3 вхождениям предыдущего символа или группы;
# {0,} соответствует нескольким вхождениям предыдущего символа или группы либо не соответствует ни одному.

# Другой пример: шаблон a{3} будет соответствовать строке aaa, но не aa или aaaa.
# Шаблон a{1,3} будет соответствовать строкам a, aa или aaa, но не aaaa

# Вы также можете использовать фигурные скобки с метасимволом точки, чтобы сопоставить любой символ
# определенное количество раз. Например, шаблон .{3} будет соответствовать любым трем символам в строке,
# а шаблон .{2,4} будет соответствовать любым 2–4 символам в строке.

